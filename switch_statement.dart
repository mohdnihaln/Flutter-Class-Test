void main(){

var grade = 'a'.toUpperCase();

switch (grade) {

  case 'A':

    print('Excellent!');

    break;

  case 'B':

    print('Good!');

    break;

  case 'C':

    print('Fair!');

    break;

  case 'D':

    print('Needs Improvement!');

    break;

  default:

    print('Invalid');

}

}